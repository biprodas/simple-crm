import { Loader2 } from "lucide-react";
import { z } from "zod";

import {
  Sheet,
  SheetContent,
  SheetDescription,
  SheetHeader,
  SheetTitle,
} from "~/components/ui/sheet";
import {
  useGetCustomerQuery,
  useUpdateCustomerMutation,
} from "../apis/queries";
import { useOpenCustomer } from "../hooks/use-open-customer";
import { CustomerForm } from "./customer-form";

export const formSchema = z.object({
  name: z.string().min(1, {
    message: "Name is required",
  }),
  acronym: z.string().optional(),
});

type FormValues = z.infer<typeof formSchema>;

export const EditCustomerSheet = () => {
  const { isOpen, onClose, id } = useOpenCustomer();

  const customerQuery = useGetCustomerQuery(id);
  const editMutation = useUpdateCustomerMutation();

  const defaultValues = {
    name: customerQuery.data?.data.name || "",
  };

  const isLoading = customerQuery.isLoading;

  const onSubmit = (values: FormValues) => {
    console.log("values", values);
    editMutation.mutate(
      { ...values, id },
      {
        onSuccess: () => {
          onClose();
        },
      }
    );
  };

  return (
    <Sheet open={isOpen} onOpenChange={onClose}>
      <SheetContent className="space-y-4">
        <SheetHeader>
          <SheetTitle>Edit Customer</SheetTitle>

          <SheetDescription>Edit an existing customer.</SheetDescription>
        </SheetHeader>

        {isLoading ? (
          <div className="absolute inset-0 flex items-center justify-center">
            <Loader2 className="size-4 animate-spin text-muted-foreground" />
          </div>
        ) : (
          <CustomerForm
            id={id}
            defaultValues={defaultValues}
            onSubmit={onSubmit}
            disabled={editMutation.isPending}
          />
        )}
      </SheetContent>
    </Sheet>
  );
};
